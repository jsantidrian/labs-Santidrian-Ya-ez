"""![plaza se mueve](https://media0.giphy.com/media/ZAzlopoHETs5lZQ6EZ/giphy.gif "plaza")

Para realizar este ejercicio utilizaremos un m√©todo super b√°sico para la eliminaci√≥n de fondo. Para esto sigue la siguiente receta:
 1. Pase a escala de grises todas las im√°genes secuenciales.
 2. Almacene en una variable la resta de las im√°genes consecutivas.

$$ imagen\_out = imagen(t) - imagen(t+1) $$

3. Establezca un umbral para eliminar algunos artefactos. Pruebe con al menos 3 umbrales.

$$ imagen\_out = imagen\_out> Umbral $$

**Nota**: No es necesario que construya una clase para esta parte.
"""

# Funci√≥n para detectar movimiento en una secuencia de im√°genes
def detectar_movimiento(secuencia_imagenes, umbral):
    #convertir a escala de grises
    def a_escala_grises(imagen):
        pesos = np.array([0.299, 0.587, 0.114])
        return np.dot(imagen, pesos).astype('uint8')
    
    #im√°genes a escala de grises
    imagenes_grises = [a_escala_grises(imagen) for imagen in secuencia_imagenes]
    #diferencia entre pares de im√°genes consecutivas
    diferencias = np.abs(np.diff(imagenes_grises, axis=0)).astype(np.int16)
    
    # Aplicar umbralizaci√≥n para obtener una imagen binaria indicando movimiento
    imagenes_binarias = [(255 * (diferencia > umbral)).astype(np.uint8) for diferencia in diferencias]
    return imagenes_binarias

